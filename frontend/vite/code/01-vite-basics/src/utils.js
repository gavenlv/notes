/**
 * 加法函数
 * @param {number} a - 第一个加数
 * @param {number} b - 第二个加数
 * @returns {number} - 两数之和
 */
export function add(a, b) {
  return a + b
}

/**
 * 乘法函数
 * @param {number} a - 第一个乘数
 * @param {number} b - 第二个乘数
 * @returns {number} - 两数之积
 */
export function multiply(a, b) {
  return a * b
}
